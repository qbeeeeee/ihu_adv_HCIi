package com.example.testingqbee;

import androidx.annotation.NonNull;
import androidx.appcompat.app.AppCompatActivity;
import androidx.drawerlayout.widget.DrawerLayout;
import androidx.fragment.app.FragmentTransaction;

import android.content.Intent;
import android.os.Bundle;
import android.view.MenuItem;

import com.google.android.gms.maps.CameraUpdateFactory;
import com.google.android.gms.maps.GoogleMap;
import com.google.android.gms.maps.OnMapReadyCallback;
import com.google.android.gms.maps.SupportMapFragment;
import com.google.android.gms.maps.model.LatLng;
import com.google.android.gms.maps.model.MarkerOptions;
import com.google.android.material.navigation.NavigationView;

public class QueryActivity extends AppCompatActivity implements OnMapReadyCallback, QueryFragment.OnbundleSendListener {

    GoogleMap gmap;
    NavigationView navigationView;
    DrawerLayout drawerLayout;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_query);
    }

    @Override
    public void onMapReady(@NonNull GoogleMap googleMap) {
        googleMap.addMarker(new MarkerOptions()
                .position(new LatLng(0, 0))
                .title("Marker"));
    }

    @Override
    public void onBundleSendListener(Bundle b) {
        MapsFragment mapsFragment = new MapsFragment();
        mapsFragment.setArguments(b);
        FragmentTransaction ft = getSupportFragmentManager().beginTransaction()
                .replace(R.id.fragmentContainerView5, mapsFragment, null);
        ft.addToBackStack(null);
        ft.commit();
    }
}